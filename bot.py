import logging
import os
import re
import requests
from aiogram import Bot, Dispatcher, types
from aiogram.utils import executor

logging.basicConfig(level=logging.INFO)

API_TOKEN = os.getenv("BOT_TOKEN")
bot = Bot(token=API_TOKEN)
dp = Dispatcher(bot)

def get_birth_date(player_name):
    url = f"https://ru.wikipedia.org/api/rest_v1/page/summary/{player_name}"
    r = requests.get(url)
    if r.status_code != 200:
        return "‚ö†Ô∏è –ù–µ —É–¥–∞–ª–æ—Å—å –Ω–∞–π—Ç–∏ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é."
    data = r.json()
    text = data.get("extract", "")
    match = re.search(r"—Ä–æ–¥–∏–ª[–∞-—è]+(?:\\s+)(\\d{1,2}\\s+[–∞-—è]+\\s+\\d{4})", text)
    if match:
        return f"üìÖ –†–æ–¥–∏–ª—Å—è {match.group(1)}"
    else:
        return "üì≠ –î–∞—Ç–∞ —Ä–æ–∂–¥–µ–Ω–∏—è –Ω–µ –Ω–∞–π–¥–µ–Ω–∞."

@dp.message_handler(commands=['start', 'help'])
async def start(message: types.Message):
    await message.answer("‚öΩ –ù–∞–ø–∏—à–∏ –∏–º—è —Ñ—É—Ç–±–æ–ª–∏—Å—Ç–∞ (–Ω–∞–ø—Ä–∏–º–µ—Ä: –°–∞–ª–∞—Ö, –ú–µ—Å—Å–∏, –†–æ–Ω–∞–ª–¥—É) ‚Äî —è —Å–∫–∞–∂—É –¥–∞—Ç—É —Ä–æ–∂–¥–µ–Ω–∏—è!")

@dp.message_handler()
async def handle_name(message: types.Message):
    name = message.text.strip().capitalize()
    info = get_birth_date(name)
    await message.answer(info)

if __name__ == "__main__":
    executor.start_polling(dp, skip_updates=True)
